/** 把零件横向拼成一整条（零件之间留 1 列间隙）。 */
function compose(keys: string[]): string[] {
  const rows: string[] = []
  for (let r = 0; r < MOOD_FACE_ROWS; r++) {
    rows.push(keys.map((k) => PARTS[k][r]).join("0"))
  }
  return rows
}

