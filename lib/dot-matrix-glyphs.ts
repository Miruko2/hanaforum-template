const BLANK_ROWS = GLYPHS[DM_BLANK]
const UNKNOWN_ROWS = GLYPHS[DM_UNKNOWN]

/**
 * 取某字符的 5×7 字模；字母自动转大写。
 * 空字符（占位）→ 全灭；已收录字符 → 其字模；其余未知字符 → 空心方框（不留空白，给可见反馈）。
 */
export function getGlyph(ch: string): string[] {
  if (!ch) return BLANK_ROWS
  return GLYPHS[ch] || GLYPHS[ch.toUpperCase()] || UNKNOWN_ROWS
}

