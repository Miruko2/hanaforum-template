-- ============================================================
-- 移除置顶帖子功能：删除 pinned_posts 表
-- ------------------------------------------------------------
-- 背景：置顶帖子功能已下线，相关前端逻辑、UI、评论门控均已移除。
-- 本脚本删除支撑该功能的数据库表。
--
-- 安全说明：
--   - pinned_posts 是叶子表：它通过外键引用 posts(id) 与 auth.users(id)，
--     但没有任何其它表引用 pinned_posts，删除它不会波及 posts / 用户数据。
--   - CASCADE 仅用于一并清除该表自身的 RLS 策略与索引；
--     因无外部依赖，不会向外级联删除其它对象。
--   - 仅丢失「哪些帖子被置顶」这一元数据，符合下线预期。
--
-- 建议执行顺序：先部署「不再访问 pinned_posts」的前端与边缘函数(moderate-text)，
--   最后再执行本脚本删表，避免线上旧代码查询已删除的表。
--   （即便顺序反了也不致命：前端对置顶查询有降级处理，
--     边缘函数的删除调用错误被忽略、不阻断删帖。）
-- ============================================================

DROP TABLE IF EXISTS public.pinned_posts CASCADE;
